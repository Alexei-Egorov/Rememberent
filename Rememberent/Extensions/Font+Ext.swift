import SwiftUI

extension Font {
    
    static func sfProTextRegular(size: CGFloat) -> Font {
        Font.custom("SFProText-Regular", size: size)
    }
    
    static func sfProTextMedium(size: CGFloat) -> Font {
        Font.custom("SFProText-Medium", size: size)
    }
    
    static func sfProTextBold(size: CGFloat) -> Font {
        Font.custom("SFProText-Bold", size: size)
    }
}
